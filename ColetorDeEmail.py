import requests
from bs4 import BeautifulSoup

def coletar_emails(url):
    # Fazendo uma solicitação GET para a URL
    response = requests.get(url)
    
    # Verificando se a solicitação foi bem-sucedida
    if response.status_code == 200:
        # Criando um objeto BeautifulSoup para analisar o conteúdo HTML
        soup = BeautifulSoup(response.content, 'html.parser')
        
        # Encontrando todas as tags span com a classe 'lowercase'
        spans = soup.find_all('span', class_='lowercase')
        
        # Lista para armazenar os e-mails coletados
        emails = []
        
        # Iterando sobre as tags span encontradas
        for span in spans:
            # Verificando se há um link 'mailto:'
            link = span.find('a', href=True)
            if link and link['href'].startswith('mailto:'):
                # Extraindo o e-mail do link 'mailto:'
                email = link['href'][7:]  # Ignora os primeiros 7 caracteres ('mailto:')
                emails.append(email)
        
        # Retorna a lista de e-mails coletados
        return emails
    else:
        # Se a solicitação não foi bem-sucedida, imprime uma mensagem de erro
        print("Erro ao acessar a URL:", response.status_code)
        return None

def salvar_emails(emails, nome_arquivo):
    # Escrevendo os e-mails em um arquivo de texto
    with open(nome_arquivo, 'w') as arquivo:
        for email in emails:
            arquivo.write(email + '\n')

